#include <iostream>
using namespace std;

int main()
{
    int integer = 10;                  // Integer data type
    char character = 'A';              // Character data type
    float floatingPoint = 5.75;        // Floating-point data type
    double largeDecimal = 8.123456789; // Double data type for higher precision
    bool boolean = true;               // Boolean data type (true or false)

    cout << "Integer: " << integer << endl;     // Print integer
    cout << "Character: " << character << endl; // Print character
    cout << "Float: " << floatingPoint << endl; // Print float
    cout << "Double: " << largeDecimal << endl; // Print double
    cout << "Boolean: " << boolean << endl;     // Print boolean
    return 0;
}
