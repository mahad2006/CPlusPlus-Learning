#include <iostream>
using namespace std;

int main()
{
    unsigned int positiveNumber = 150;       // Unsigned integer (no negative values)
    short int smallNumber = -5;              // Short integer (smaller size)
    long long int largeNumber = 10000000000; // Long long integer for large values

    // Print the values of different types
    cout << "Unsigned int: " << positiveNumber << endl;
    cout << "Short int: " << smallNumber << endl;
    cout << "Long long int: " << largeNumber << endl;
    return 0;
}
